    def initialize(self) -> dict:
        """
        Full engine initialization sequence.
        Returns a status dict consumed by the UI health panel.

        Call this once at startup. Subsequent inference calls are async.
        """
        logger.info("═══ Aura-NPU Engine Initialization ═══")
        results = {
            "vlm_loaded": False,
            "asr_loaded": False,
            "tts_loaded": False,
            "npu_status": {},
        }

        # 1. Hardware detection
        results["npu_status"] = self.verify_npu_status()

        # 2. Load VLM (try Lemonade first, fall back to direct ORT)
        results["vlm_loaded"] = (
            self._load_vlm_via_lemonade() or self._load_vlm_direct_ort()
        )

        # 3. Load ASR and TTS
        results["asr_loaded"] = self._load_asr_model()
        results["tts_loaded"] = self._load_tts_model()

        self._is_initialized = True
        logger.info(
            "Engine init complete | VLM=%s | ASR=%s | TTS=%s",
            results["vlm_loaded"],
            results["asr_loaded"],
            results["tts_loaded"],
        )
        return results

    # ── Core Inference: Vision-Language Reasoning ──────────────────────────────

    async def analyze_image_async(
        self,
        image: Any,
        prompt: str,
        language: str = "en",
        max_tokens: int = 512,
    ) -> InferenceResult:
        """
        Asynchronous vision-language inference.
        Accepts PIL.Image or numpy array, routes to Gemma-3 VLM on NPU.

        This is the "money shot" function that causes the NPU utilization
        spike visible in Windows Task Manager.

        Args:
            image: PIL.Image, numpy array, or file path to image.
            prompt: Natural language question about the image.
            language: BCP-47 code for response language (e.g., 'hi' for Hindi).
            max_tokens: Maximum response length.

        Returns:
            InferenceResult with text, latency, and NPU metrics.
        """
        loop = asyncio.get_event_loop()
        return await loop.run_in_executor(
            None,
            self._run_vlm_inference,
            image,
            prompt,
            language,
            max_tokens,
        )

    def _run_vlm_inference(
        self,
        image: Any,
        prompt: str,
        language: str,
        max_tokens: int,
    ) -> InferenceResult:
        """
        Synchronous VLM inference kernel.
        Executed in a thread pool by analyze_image_async().

        Routes through Lemonade SDK if available, else direct ORT session.
        """
        result = InferenceResult()
        start = time.perf_counter()

        # Build the vision prompt with language instruction
        system_prompt = self._build_vision_prompt(prompt, language)

        try:
            # ── Path A: Lemonade SDK (preferred) ──────────────────────────────
            if self._lemonade_model is not None:
                image_processed = self._preprocess_image(image)
                response = (
                    self._lemonade_model
                    .OrtGenAiPrompt(
                        prompt=system_prompt,
                        images=[image_processed],
                        max_new_tokens=max_tokens,
                    )
                    .response
                )
                result.text = response
                result.provider_used = "Lemonade SDK → VitisAI EP (NPU)"

            # ── Path B: Direct ORT Session ─────────────────────────────────
            elif self._vlm_session is not None:
                # Preprocess image to model input format
                image_tensor = self._preprocess_image_to_tensor(image)
                prompt_tokens = self._tokenize_prompt(system_prompt)

                ort_inputs = {
                    "input_ids": prompt_tokens,
                    "pixel_values": image_tensor,
                    "attention_mask": np.ones(prompt_tokens.shape, dtype=np.int64),
                }

                ort_outputs = self._vlm_session.run(None, ort_inputs)
                result.text = self._decode_output(ort_outputs[0])
                result.provider_used = (
                    self._vlm_session.get_providers()[0]
                )

            else:
                result.error = (
                    "VLM not initialized. Call initialize() first or download models."
                )
                logger.error(result.error)

        except Exception as exc:
            result.error = str(exc)
            logger.error("VLM inference failed: %s", exc, exc_info=True)

        result.latency_ms = (time.perf_counter() - start) * 1000
        result.metadata = {
            "language": language,
            "prompt_length": len(prompt),
            "max_tokens": max_tokens,
            "timestamp": time.time(),
        }

        logger.debug(
            "VLM inference | %.1fms | provider=%s | tokens=%d",
            result.latency_ms,
            result.provider_used,
            result.tokens_generated,
        )
        return result

