# ── Core NPU Engine ────────────────────────────────────────────────────────────

class AuraNPUEngine:
    """
    Primary NPU orchestration engine for Aura-NPU.

    Manages three inference pipelines:
      1. Gemma-3 4B VLM  — vision-language reasoning via Lemonade SDK
      2. IndicConformer   — multilingual ASR (22 Indian languages)
      3. Svara-TTS        — regional voice synthesis

    All inference runs 100% offline on the AMD Ryzen AI XDNA 2 NPU.
    Zero cloud calls. Zero data leakage.
    """

    def __init__(self, config: Optional[NPUConfig] = None):
        self.config = config or NPUConfig()
        self._vlm_session: Optional[Any] = None
        self._asr_session: Optional[ort.InferenceSession] = None
        self._tts_session: Optional[ort.InferenceSession] = None
        self._lemonade_model: Optional[Any] = None
        self._inference_queue: asyncio.Queue = asyncio.Queue(maxsize=10)
        self._is_initialized: bool = False
        self._npu_detected: bool = False
        self._driver_version: str = "unknown"

        # Ensure cache directory exists
        os.makedirs(self.config.cache_dir, exist_ok=True)

        logger.info("AuraNPUEngine instantiated. Call initialize() before inference.")

    # ── Hardware Detection ─────────────────────────────────────────────────────

    def verify_npu_status(self) -> dict:
        """
        Probe the system for AMD Ryzen AI NPU hardware.
        Returns a structured status dict suitable for the UI health panel.
        """
        status = {
            "npu_detected": False,
            "xdna_version": "unknown",
            "npu_tops": 0,
            "driver_version": "unknown",
            "vitisai_ep_available": False,
            "lemonade_available": LEMONADE_AVAILABLE,
            "ort_available": ORT_AVAILABLE,
            "errors": [],
        }

        # Check for VitisAI EP availability in onnxruntime
        if ORT_AVAILABLE:
            available_providers = ort.get_available_providers()
            if "VitisAIExecutionProvider" in available_providers:
                status["vitisai_ep_available"] = True
                status["npu_detected"] = True
                self._npu_detected = True
                logger.info("✅ VitisAIExecutionProvider detected in ONNX Runtime providers.")
            else:
                status["errors"].append(
                    "VitisAIExecutionProvider not in ort.get_available_providers(). "
                    "Install onnxruntime-vitisai and ensure Ryzen AI drivers are installed."
                )
                logger.warning(
                    "Available ORT providers: %s", available_providers
                )

        # Probe xclbin file existence (confirms driver installation)
        if Path(self.config.xclbin_path).exists():
            status["xdna_version"] = "XDNA 2 (Strix Point / Strix Halo)"
            status["npu_tops"] = 50
            logger.info("✅ xclbin bitstream found at %s", self.config.xclbin_path)
        else:
            msg = (
                f"xclbin not found at {self.config.xclbin_path}. "
                "Ryzen AI SW 1.7 may not be installed correctly."
            )
            status["errors"].append(msg)
            logger.warning(msg)

        # Pull driver version from Windows registry (best-effort)
        try:
            import winreg
            key_path = r"SYSTEM\CurrentControlSet\Services\amdxdna"
            reg_key = winreg.OpenKey(winreg.HKEY_LOCAL_MACHINE, key_path)
            version, _ = winreg.QueryValueEx(reg_key, "ImagePath")
            status["driver_version"] = version
            self._driver_version = version
            winreg.CloseKey(reg_key)
        except Exception:
            # Non-critical: driver version is cosmetic for the demo
            status["driver_version"] = "Check Device Manager > NPU Compute Accelerators"

        return status

    def _build_vitisai_provider_options(self) -> dict:
        """
        Construct the VitisAI EP provider options dict.
        These options are passed directly to ort.InferenceSession.
        Strictly validated for Ryzen AI 300 series silicon.
        """
        return {
            "config_file": self.config.config_file,
            "cacheDir": self.config.cache_dir,
            "cacheKey": "aura_npu_v1",
            "num_of_dpu_runners": str(self.config.num_of_dpu_runners),
            # Enable XDNA 2-specific tile scheduling
            "ai_analyzer_visualization": "0",
            # NPU DMA transfer optimization for Strix Point
            "enable_tiling": "1",
        }

    # ── Lemonade SDK: Gemma-3 VLM Initialization ──────────────────────────────

    def _load_vlm_via_lemonade(self) -> bool:
        """
        Use the AMD Lemonade SDK to load Gemma-3 4B VLM on the NPU.
        Lemonade abstracts the VitisAI EP complexity for generative models.

        SDK Reference:
          from lemonade.api import from_pretrained
          Uses OrtGenAiLoad tool with hybrid CPU-NPU execution plan.
        """
        if not LEMONADE_AVAILABLE:
            logger.warning("Lemonade SDK unavailable. Falling back to direct ONNX session.")
            return False

        try:
            logger.info("Loading Gemma-3 4B VLM via Lemonade SDK → NPU ...")
            start = time.perf_counter()

            # from_pretrained points the model to the NPU via Lemonade's
            # device mapping. OrtGenAiLoad selects VitisAI EP automatically
            # when running on Ryzen AI silicon.
            self._lemonade_model = (
                from_pretrained(
                    self.config.gemma3_vlm_path,
                    tools=[OrtGenAiLoad, OrtGenAiPrompt],
                    device="npu",  # Lemonade device selector: "npu" → VitisAI EP
                    dtype="int8",
                )
                .OrtGenAiLoad()  # Compiles the ONNX graph to XDNA 2 AIE tiles
            )

            elapsed = (time.perf_counter() - start) * 1000
            logger.info(
                "✅ Gemma-3 4B VLM loaded via Lemonade SDK in %.1fms", elapsed
            )
            return True

        except Exception as exc:
            logger.error("Lemonade VLM load failed: %s", exc, exc_info=True)
            return False

    def _load_vlm_direct_ort(self) -> bool:
        """
        Fallback: Load Gemma-3 VLM directly via onnxruntime with VitisAI EP.
        Used when Lemonade SDK is unavailable.
        """
        if not ORT_AVAILABLE:
            logger.error("Cannot load VLM: onnxruntime not installed.")
            return False

        model_path = self.config.gemma3_vlm_path
        if not Path(model_path).exists():
            logger.error(
                "VLM model not found at %s. Run: python scripts/download_models.py",
                model_path,
            )
            return False

        try:
            logger.info("Loading Gemma-3 4B VLM via direct ORT + VitisAI EP ...")
            start = time.perf_counter()

            providers = [
                (
                    "VitisAIExecutionProvider",
                    self._build_vitisai_provider_options(),
                ),
                # CPU fallback for ops not supported by XDNA 2
                "CPUExecutionProvider",
            ]

            so = ort.SessionOptions()
            so.log_severity_level = 3  # Warnings only
            so.enable_profiling = False

            self._vlm_session = ort.InferenceSession(
                model_path,
                sess_options=so,
                providers=providers,
            )

            elapsed = (time.perf_counter() - start) * 1000
            active_provider = self._vlm_session.get_providers()[0]
            logger.info(
                "✅ Gemma-3 4B VLM loaded via ORT in %.1fms | Provider: %s",
                elapsed,
                active_provider,
            )
            return True

        except Exception as exc:
            logger.error("Direct ORT VLM load failed: %s", exc, exc_info=True)
            return False

    def _load_asr_model(self) -> bool:
        """Load IndicConformer ASR model for 22-language speech recognition."""
        if not ORT_AVAILABLE:
            return False

        model_path = self.config.indic_conformer_path
